package com.ishan.leetcode.company.chewypractice.parkinglot;

import java.util.Objects;

/*
 * Vehicle: Represents a vehicle and its attributes.
 */
public class Vehicle {
    String licensePlateNumber;


    //todo can be an enum
    String vehicleType;

    String[] additionalInformation;

    public Vehicle(String licensePlateNumber, String vehicleType) {
        this.licensePlateNumber = licensePlateNumber;
        this.vehicleType = vehicleType;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Vehicle vehicle = (Vehicle) o;
        return Objects.equals(licensePlateNumber, vehicle.licensePlateNumber);
    }

    @Override
    public int hashCode() {
        return Objects.hash(licensePlateNumber);
    }

    public String getLicensePlateNumber() {
        return licensePlateNumber;
    }

    public void setLicensePlateNumber(String licensePlateNumber) {
        this.licensePlateNumber = licensePlateNumber;
    }

    public String getVehicleType() {
        return vehicleType;
    }

    public void setVehicleType(String vehicleType) {
        this.vehicleType = vehicleType;
    }
}
